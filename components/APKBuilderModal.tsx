function genManifest(appName: string, themeColor: string): string {
  const short = appName.split(/\s+/)[0].slice(0, 12);
  return JSON.stringify({
    name: appName,
    short_name: short,
    start_url: "./",
    display: "standalone",
    orientation: "portrait",
    background_color: themeColor,
    theme_color: themeColor,
    icons: [
      { src: "icon.svg", sizes: "any", type: "image/svg+xml", purpose: "any maskable" },
    ],
  }, null, 2);
}

function genIconSvg(appName: string, themeColor: string): string {
  const initial = (appName || "A").charAt(0).toUpperCase();
  // pick a good contrasting text color
  const bg = themeColor.replace("#", "");
  const r = parseInt(bg.slice(0, 2), 16) || 13;
  const g = parseInt(bg.slice(2, 4), 16) || 17;
  const b = parseInt(bg.slice(4, 6), 16) || 23;
  const lum = 0.299 * r + 0.587 * g + 0.114 * b;
  const fg = lum > 128 ? "#000000" : "#ffffff";
  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 192 192">
  <rect width="192" height="192" rx="32" fill="${themeColor}"/>
  <text x="96" y="130" font-family="Arial,sans-serif" font-weight="bold"
    font-size="100" text-anchor="middle" fill="${fg}">${initial}</text>
</svg>`;
}

