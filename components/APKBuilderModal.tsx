/** Capacitor workflow — usado para HTML/JS e apps Vite buildados.
 *  Resolve os erros de Gradle usando android-actions/setup-android
 *  e versões fixas do Capacitor em vez do Bubblewrap. */
function genCapacitorWorkflow(
  appName: string,
  packageId: string,
  themeColor: string,
  webDir: string,
  preBuildCmd?: string,
): string {
  const initial = (appName || "A").charAt(0).toUpperCase();
  const wd = webDir === "." ? "" : `${webDir}/`;
  const gradleDir = webDir === "." ? "android" : `${webDir}/android`;

  return `name: "\uD83E\uDD16 Build APK Android"
on:
  workflow_dispatch:
  push:
    branches: [main, master]
permissions:
  contents: write
env:
  APP_NAME: "${appName.replace(/"/g, '\\"')}"
  PACKAGE_ID: "${packageId}"
  THEME_COLOR: "${themeColor}"
jobs:
  build-apk:
    runs-on: ubuntu-22.04
    steps:
      - name: Checkout
        uses: actions/checkout@v4

      - name: Setup Java 17
        uses: actions/setup-java@v4
        with:
          java-version: "17"
          distribution: temurin

      - name: Setup Android SDK
        uses: android-actions/setup-android@v3

      - name: Setup Node 20
        uses: actions/setup-node@v4
        with:
          node-version: "20"
${preBuildCmd ? `
      - name: Instalar dependencias e buildar
        run: |
          ${preBuildCmd}
` : ""}
      - name: Gerar icones com ImageMagick
        run: |
          sudo apt-get install -y imagemagick -q 2>/dev/null
          INIT="${initial}"
          FONT="/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf"
          [ -f "$FONT" ] || FONT="DejaVu-Sans-Bold"
          gen_icon() {
            SZ=$1; OUT=$2
            convert -size ${SZ}x${SZ} xc:"$THEME_COLOR" \\
              -fill white -font "$FONT" \\
              -pointsize $((SZ / 2)) -gravity center \\
              -annotate 0 "$INIT" "$OUT" 2>/dev/null || \\
            convert -size ${SZ}x${SZ} xc:"$THEME_COLOR" \\
              -fill white -pointsize $((SZ / 2)) -gravity center \\
              -draw "text 0,0 '$INIT'" "$OUT" 2>/dev/null || true
          }
          gen_icon 192 ${wd}icon-192.png
          gen_icon 512 ${wd}icon-512.png
          echo "Icones gerados"

      - name: Gerar keystore de assinatura
        run: |
          keytool -genkey -v \\
            -keystore release.keystore -alias app \\
            -keyalg RSA -keysize 2048 -validity 10000 \\
            -storepass android123 -keypass android123 \\
            -dname "CN=$APP_NAME, OU=App, O=DevMobile, L=BR, ST=SP, C=BR"

      - name: Instalar Capacitor (versoes fixas)
        working-directory: ${webDir === "." ? "." : webDir}
        run: |
          [ -f package.json ] || echo '{"name":"app","version":"1.0.0","private":true}' > package.json
          npm install --save-exact \\
            @capacitor/cli@6.2.0 \\
            @capacitor/core@6.2.0 \\
            @capacitor/android@6.2.0
          cat > capacitor.config.json << 'CAPEOF'
          {
            "appId": "$PACKAGE_ID",
            "appName": "$APP_NAME",
            "webDir": ".",
            "server": { "androidScheme": "https" }
          }
          CAPEOF

      - name: Adicionar plataforma Android
        working-directory: ${webDir === "." ? "." : webDir}
        run: |
          npx cap add android 2>&1 || true
          npx cap copy android 2>&1
          cp ../release.keystore android/app/release.keystore 2>/dev/null || \\
          cp release.keystore android/app/release.keystore 2>/dev/null || \\
          cp ../../release.keystore android/app/release.keystore || true

      - name: Build APK com Gradle
        working-directory: ${gradleDir}
        run: |
          chmod +x gradlew
          ./gradlew assembleRelease \\
            -Pandroid.injected.signing.store.file=app/release.keystore \\
            -Pandroid.injected.signing.store.password=android123 \\
            -Pandroid.injected.signing.key.alias=app \\
            -Pandroid.injected.signing.key.password=android123 \\
            --no-daemon \\
            --stacktrace 2>&1 | tail -100
          echo "APK gerado com sucesso!"

      - name: Upload APK
        uses: actions/upload-artifact@v4
        with:
          name: "APK-Android-\${{ github.run_number }}"
          path: |
            **/app/build/outputs/apk/release/*.apk
            **/app/build/outputs/apk/debug/*.apk
          if-no-files-found: warn
          retention-days: 30

      - name: Resumo
        run: |
          APK=$(find . -name "*.apk" 2>/dev/null | head -1)
          echo "## \u2705 APK Gerado!" >> $GITHUB_STEP_SUMMARY
          echo "Baixe o arquivo .apk na aba **Artifacts** acima." >> $GITHUB_STEP_SUMMARY
          echo "App: **$APP_NAME** | Package: \`$PACKAGE_ID\`" >> $GITHUB_STEP_SUMMARY
`;
}

